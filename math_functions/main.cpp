#include <iostream>
#include <cmath>
using namespace std;

int main()
{

    // this lesson we are going to learn about math functions
    cout << "The value is: " << abs(-100) << endl;
    cout << "The value is: " << pow(2, 3) << endl;
    cout << "The value is: " << sqrt(100) << endl;
    cout << "The value is: " << ceil(2.5) << endl;
    cout << "The value is: " << floor(2.5) << endl;
    cout << "The value is: " << log(2) << endl;
    cout << "The value is: " << log10(5) << endl;
    cout << "The value is: " << log2(6) << endl;
    cout << "The value is: " << exp(2) << endl;
    cout << "The value is: " << sin(9) << endl;
    cout << "The value is: " << cos(7) << endl;
    cout << "The value is: " << tan(3) << endl;
    cout << "The value is: " << asin(8) << endl;
    cout << "The value is: " << acos(5) << endl;
    cout << "The value is: " << atan(4) << endl;
    cout << "The value is: " << atan2(2, 2) << endl;
    cout << "The value is: " << hypot(5, 8) << endl;

    return 0;
}